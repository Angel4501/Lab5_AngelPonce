    private void EntrarLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EntrarLoginMouseClicked

        boolean correcto = false;
        int pos = 0;
        for (int i = 0; i < personas.size(); i++) {
            if (tfusuario.getText().equals(personas.get(i).getUsuario())
                    && pfpass.getText().equals(personas.get(i).getPass())) {
                correcto = true;
                pos = i;
                break;
            }
        }
        if (correcto == true) {
            if (personas.get(pos) instanceof CEO) {
                VerCEO.pack();
                VerCEO.setLocationRelativeTo(this);
                VerCEO.setModal(true);// Hace que me bloquee la ventana que está atrás de esta ventana
                VerCEO.setVisible(true);
            } else if (personas.get(pos) instanceof Administrativos) {
                VerAdministrativos.pack();
                VerAdministrativos.setLocationRelativeTo(this);
                VerAdministrativos.setModal(true);// Hace que me bloquee la ventana que está atrás de esta ventana
                VerAdministrativos.setVisible(true);
            } else if (personas.get(pos) instanceof PersonalConsejoAcademico) {
                VerPersonalAcademico.pack();
                VerPersonalAcademico.setLocationRelativeTo(this);
                VerPersonalAcademico.setModal(true);// Hace que me bloquee la ventana que está atrás de esta ventana
                VerPersonalAcademico.setVisible(true);
            } else if (personas.get(pos) instanceof PersonalDePlanta) {
                VerPersonalDePlanta.pack();
                VerPersonalDePlanta.setLocationRelativeTo(this);
                VerPersonalDePlanta.setModal(true);// Hace que me bloquee la ventana que está atrás de esta ventana
                VerPersonalDePlanta.setVisible(true);
            } else if (personas.get(pos) instanceof Docente) {
                VerDocente.pack();
                VerDocente.setLocationRelativeTo(this);
                VerDocente.setModal(true);// Hace que me bloquee la ventana que está atrás de esta ventana
                VerDocente.setVisible(true);
            } else if (personas.get(pos) instanceof Estudiante) {
                VerEstudiante.pack();
                VerEstudiante.setLocationRelativeTo(this);
                VerEstudiante.setModal(true);// Hace que me bloquee la ventana que está atrás de esta ventana
                VerEstudiante.setVisible(true);
            } else if (personas.get(pos) instanceof Tutores) {
                VerTutor.pack();
                VerTutor.setLocationRelativeTo(this);
                VerTutor.setModal(true);// Hace que me bloquee la ventana que está atrás de esta ventana
                VerTutor.setVisible(true);
            }
        } else if (correcto == false) {
            JOptionPane.showMessageDialog(this, "Usuario no existe");
        }
    }//GEN-LAST:event_EntrarLoginMouseClicked

